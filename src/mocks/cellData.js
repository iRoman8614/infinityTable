// Функция для получения данных ячейки
export const getCellData = (date, headerId) => {
    // Конвертируем из ISO (2025-10-27) в DD.MM.YYYY (27.10.2025)
    const isoStr = date.toISOString().split('T')[0];
    const [year, month, day] = isoStr.split('-');
    const dateStr = `${day}.${month}.${year}`;

    return mockCellData[dateStr]?.[headerId] || null;
};

// Проверка: нужно ли пропустить ячейку из-за rowspan с предыдущих дат
export const shouldSkipCellDueToRowspan = (date, headerId, allDates) => {
    const isoStr = date.toISOString().split('T')[0];
    const currentIndex = allDates.findIndex(d =>
        d.toISOString().split('T')[0] === isoStr
    );

    if (currentIndex === -1) return false;

    // Проверяем предыдущие даты на rowspan
    for (let i = 1; i < 20; i++) {
        if (currentIndex - i < 0) break;

        const prevDate = allDates[currentIndex - i];
        const prevIsoStr = prevDate.toISOString().split('T')[0];
        const [year, month, day] = prevIsoStr.split('-');
        const prevDateStr = `${day}.${month}.${year}`;
        const prevCellData = mockCellData[prevDateStr]?.[headerId];

        if (prevCellData && prevCellData.rowspan > i) {
            return true;
        }
    }

    return false;
};

// Проверка: нужно ли пропустить ячейку из-за colspan на ТЕКУЩЕЙ дате
export const shouldSkipCellDueToColspan = (date, headerIndex, leafHeaders) => {
    const isoStr = date.toISOString().split('T')[0];
    const [year, month, day] = isoStr.split('-');
    const dateStr = `${day}.${month}.${year}`;

    // Проверяем все ячейки слева от текущей на этой же дате
    for (let i = 0; i < headerIndex; i++) {
        const leftHeader = leafHeaders[i];
        const leftCellData = mockCellData[dateStr]?.[leftHeader.id];

        if (leftCellData && leftCellData.colspan > 1) {
            const spanEnd = i + leftCellData.colspan;
            if (headerIndex < spanEnd) {
                return true;
            }
        }
    }

    return false;
};

